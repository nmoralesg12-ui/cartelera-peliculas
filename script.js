// ✅ CONTENEDORES
const contenedor = document.getElementById("peliculas");
const filtroUbicacion = document.getElementById("filtroUbicacion");
const buscarGenero = document.getElementById("buscarGenero");


// ✅ CARGAR UBICACIONES
function cargarUbicaciones() {
    const ubicaciones = [...new Set(peliculas.map(p => p.Ubication))];

    ubicaciones.forEach(ubi => {
        const option = document.createElement("option");
        option.value = ubi;
        option.textContent = ubi;
        filtroUbicacion.appendChild(option);
    });
}


// ✅ CARGAR GENEROS
function cargarGeneros() {
    const generos = [...new Set(peliculas.map(p => p.Type))];

    generos.forEach(gen => {
        const option = document.createElement("option");
        option.value = gen;
        option.textContent = gen;
        buscarGenero.appendChild(option);
    });
}


// ✅ MOSTRAR PELICULAS
function mostrarPeliculas() {

    contenedor.innerHTML = "";

    const ubicacionSeleccionada = filtroUbicacion.value.toLowerCase();
    const generoSeleccionado = buscarGenero.value.toLowerCase();

    const filtradas = peliculas.filter(peli => {

        const okUbicacion =
            ubicacionSeleccionada === "" ||
            peli.Ubication.toLowerCase().includes(ubicacionSeleccionada);

        const okGenero =
            generoSeleccionado === "" ||
            peli.Type.toLowerCase().includes(generoSeleccionado);

        return okUbicacion && okGenero;
    });

    filtradas.forEach(peli => {

        let estadoTexto = peli.Estado ? "En cartelera" : "No disponible";
        let estadoColor = peli.Estado ? "success" : "danger";

        const col = document.createElement("div");
        col.classList.add("col-md-3", "mb-4");

        col.innerHTML = `
            <div class="card h-100 bg-dark text-white">

                <!-- ✅ IMAGEN CORREGIDA -->
                <img src="${corregirPoster(peli)}" class="card-img-top" alt="${peli.Title}">

                <div class="card-body">
                    <h5>${peli.Title}</h5>

                    <p>
                        📅 ${peli.Year} <br>
                        🎭 ${peli.Type} <br>
                        📍 ${peli.Ubication}
                    </p>

                    <p class="small">
                        ${peli.description}
                    </p>
                </div>

                <div class="card-footer text-center">
                    <span class="badge bg-${estadoColor}">
                        ${estadoTexto}
                    </span>
                </div>

            </div>
        `;

        contenedor.appendChild(col);
    });
}


// ✅ CORREGIR IMAGEN (ARREGLA KINGSMAN)
function corregirPoster(peli) {

    // ✅ FIX ESPECÍFICO PARA KINGSMAN
    if (peli.Title.toLowerCase().includes("kingsman")) {
        return "https://m.media-amazon.com/images/M/MV5BMjA5NTY2MjM2NF5BMl5BanBnXkFtZTgwOTA5MTY5NDE@._V1_.jpg";
    }

    return peli.Poster;
}


// ✅ EVENTOS
filtroUbicacion.addEventListener("change", mostrarPeliculas);
buscarGenero.addEventListener("change", mostrarPeliculas);


// ✅ INICIO
cargarUbicaciones();
cargarGeneros();
mostrarPeliculas();